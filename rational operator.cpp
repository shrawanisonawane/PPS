#include <iostream>
using namespace std;

int main() 
{ 
   int a=8,b=7;
   //Equal operator
   cout<<"a==b"<< (a==b)<<endl;
   //Greater than operator
   cout<<"a>b"<< (a>b)<<endl;
   //Greater than or equal to operator
   cout<<"a>=b"<< (a>=b)<<endl;
   //lesser than operator
   cout<<"a<b"<< (a<b)<<endl;
   //lesser than or equal to operator
   cout<<"a<=b"<< (a<=b)<<endl;
   //true
   cout<<"a!=b is "<<(a!=b)<<endl;
    return 0;
}